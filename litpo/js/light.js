$(document).ready(function(){
	$("#details").fadeIn(3000);	
	$("#logo").mouseenter(function(){
		$("#logo").attr("src","images/light_on.png");
	});
	$("#logo").mouseleave(function(){
		$("#logo").attr("src","images/light_off.png");
	});
	$("#logo_m").mouseenter(function(){
		$("#logo_m").attr("src","images/light_on.png");
	});
	$("#logo_m").mouseleave(function(){
		$("#logo_m").attr("src","images/light_off.png");
	});
});